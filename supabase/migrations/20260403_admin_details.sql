-- Create admin_documents table
CREATE TABLE IF NOT EXISTS admin_documents (
  id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  document_name VARCHAR(255) NOT NULL,
  document_type VARCHAR(50) NOT NULL CHECK (document_type IN ('pdf', 'word', 'excel')),
  file_path VARCHAR(500) NOT NULL,
  file_size BIGINT,
  uploaded_by UUID REFERENCES auth.users(id) ON DELETE CASCADE,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()),
  updated_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

-- Create unified admin_contacts table for both contacts and patrons
CREATE TABLE IF NOT EXISTS admin_contacts (
  id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  contact_type VARCHAR(50) NOT NULL CHECK (contact_type IN ('contact', 'patron')),
  name VARCHAR(255) NOT NULL,
  batch INTEGER,
  contact_no VARCHAR(20),
  patron_no VARCHAR(50),
  description TEXT,
  created_by UUID REFERENCES auth.users(id) ON DELETE CASCADE,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()),
  updated_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

