-- Set up RLS (Row Level Security) policies for admin_documents
ALTER TABLE admin_documents ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Allow super_admin to view and manage admin documents" ON admin_documents
  FOR ALL USING (
    EXISTS (
      SELECT 1 FROM members
      WHERE members.auth_user_id = auth.uid() AND members.role = 'super_admin'
    )
  );

-- Set up RLS for unified admin_contacts table
ALTER TABLE admin_contacts ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Allow super_admin to view and manage admin contacts" ON admin_contacts
  FOR ALL USING (
    EXISTS (
      SELECT 1 FROM members
      WHERE members.auth_user_id = auth.uid() AND members.role = 'super_admin'
    )
  );

-- Create indexes for better query performance
CREATE INDEX IF NOT EXISTS idx_admin_documents_uploaded_by ON admin_documents(uploaded_by);
CREATE INDEX IF NOT EXISTS idx_admin_documents_created_at ON admin_documents(created_at);
CREATE INDEX IF NOT EXISTS idx_admin_contacts_type_created ON admin_contacts(contact_type, created_at);
CREATE INDEX IF NOT EXISTS idx_admin_contacts_created_by ON admin_contacts(created_by);
CREATE INDEX IF NOT EXISTS idx_admin_contacts_batch ON admin_contacts(batch);
CREATE INDEX IF NOT EXISTS idx_admin_contacts_patron_no ON admin_contacts(patron_no);
